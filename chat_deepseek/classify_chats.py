def classify(name: str) -> str:
    name_lower = name.lower()
    for cat, data in SIGNATURES.items():
        for pattern in data["match"]:
            if pattern in name_lower:
                return cat
    return "Other"

def main():
    results = []
    for md_file in sorted(ROOT.glob("2026-*/**/*.md")):
        rel = str(md_file.relative_to(ROOT))
        parts = md_file.parts
        date_dir = parts[-2]  # e.g., 2026-05-15
        stem = md_file.stem
        category = classify(stem)
        results.append({
            "file": rel,
            "date": date_dir,
            "name": stem,
            "category": category
        })

    # Статистика
    from collections import Counter
    stats = Counter(r["category"] for r in results)

    output = {
        "total": len(results),
        "categories": dict(stats),
        "chats": results
    }

    OUT.write_text(json.dumps(output, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"✅ Классифицировано {len(results)} чатов → {OUT}")
    print("\n📊 Статистика:")
    for cat, count in stats.most_common():
        print(f"  {cat}: {count}")

if __name__ == "__main__":
    main()